package dto;

public class FindingPetInfo {
	String city;
	String region;
	String wherePlaced;
	String photo;
	String petType;
	String petSpecies;
	String specialFiture;
	String size;
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getWherePlaced() {
		return wherePlaced;
	}
	public void setWherePlaced(String wherePlaced) {
		this.wherePlaced = wherePlaced;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getPetType() {
		return petType;
	}
	public void setPetType(String petType) {
		this.petType = petType;
	}
	public String getPetSpecies() {
		return petSpecies;
	}
	public void setPetSpecies(String petSpecies) {
		this.petSpecies = petSpecies;
	}
	public String getSpecialFiture() {
		return specialFiture;
	}
	public void setSpecialFiture(String specialFiture) {
		this.specialFiture = specialFiture;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}

	
}
